//2.	WAP in Java to understand the difference between print() and println().
public class PrintVsPrintln {
    public static void main(String[] args) {
        System.out.print("This is using print().");
        System.out.print(" It doesn't add a newline.");

        System.out.println("This is using println().");
        System.out.println(" It adds a newline after printing.");
    }
}
